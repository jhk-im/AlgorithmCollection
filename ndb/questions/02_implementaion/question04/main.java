import java.util.Arrays;

public class Main {
    public static void main(String[] args) {
        Solution solution = new Solution();
        int[][] key = {{0, 0, 0}, {1, 0, 0}, {0, 1, 1}};
        int[][] lock = {{1, 1, 1}, {1, 1, 0}, {1, 0, 1}};
        System.out.println(solution.solution(key, lock));
    }
}

class Solution {
    public boolean solution(int[][] key, int[][] lock) {
        int n = lock.length;
        // 기존 자물쇠 크기의 3배 이차원 배열 생성
        int length = n * 3;
        int[][] newLock = new int[length][length];
        for (int i = 0; i < n; i++) {
            System.arraycopy(lock[i], 0, newLock[n + i], n, n);
        }
        System.out.println("---new lock---");
        for (int[] k : newLock) {
            System.out.println(Arrays.toString(k));
        }
        // 4가지 방향 확인
        for (int i = 0; i < 4; i++) {
            key = rotation(key); // 열쇠 회전
            // 자물쇠 끼워넣기
            for (int x = 1; x < n * 2; x++) {
                for (int y = 1; y < n * 2; y++) {
                    updateLock(newLock, key, x, y);
                    // 열쇠가 들어맞는지 검사
                    if (check(newLock)) {
                        return true;
                    }
                    // 자물쇠 초기화
                    newLock = reset(newLock.length, lock);
                }
            }
        }
        return false;
    }

    // 자물쇠가 모두 1인지 확인
    public boolean check(int[][] locks) {
        int length = locks.length / 3;
        for (int i = 0; i < length; i++) {
            for (int j = 0; j < length; j++) {
                if (locks[length + i][length + j] != 1) {
                    return false;
                }
            }
        }
        return true;
    }

    // 자물쇠 맞춰 업데이트
    public void updateLock(int[][] newLock, int[][] key, int x, int y) {
        int m = key.length;
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < m; j++) {
                newLock[x + i][y + j] += key[i][j];
            }
        }
        System.out.println("---update---");
        for (int[] k : newLock) {
            System.out.println(Arrays.toString(k));
        }
    }

    // 자물쇠 초기화
    public int[][] reset(int newLockLength, int[][] lock) {
        int n = lock.length;
        int[][] temp = new int[newLockLength][newLockLength];
        for (int i = 0; i < n; i++) {
            System.arraycopy(lock[i], 0, temp[n + i], n, n);
        }

        System.out.println("---reset---");
        for (int[] k : temp) {
            System.out.println(Arrays.toString(k));
        }

        return temp;
    }

    // key 90도 회전
    public int[][] rotation(int[][] key) {
        int length = key.length;
        int[][] rotationKey = new int[length][length];
        for (int i = 0; i < length; i++) {
            for (int j = 0; j < length; j++) {
                rotationKey[i][j] = key[length - 1 - j][i];
            }
        }
        System.out.println("---rotation---");
        for (int[] k : rotationKey) {
            System.out.println(Arrays.toString(k));
        }
        return rotationKey;
    }
}

